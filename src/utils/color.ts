export function notionColor(color: string) {
    if (color == "gray") {
        return "#A4A4A4"
    }
    else if (color == "brown") {
        return "#E2A285"
    }
    else if (color == "red") {
        return "#FF8894"
    }
    else if (color == "orange") {
        return "#FFBF97"
    }
    else if (color == "yellow") {
        return "#FFE195"
    }
    else if (color == "green") {
        return "#43A892"
    }
    else if (color == "blue") {
        return "#4588C2"
    }
    else if (color == "pink") {
        return "#FF97D4"
    }
    else if (color == "purple") {
        return "#B282FF"
    }
    else {
        return "#e6e6e4"
    }
}